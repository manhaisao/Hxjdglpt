package com.xzz.hxjdglpt.model;

public class Zf {

    private long id;
    private String content;
    private String time;
    private long zfr;
    private String zfrName;
    private String jsr;
    private long taskId;

    private String jsrName;

    public String getJsrName() {
        return jsrName;
    }

    public void setJsrName(String jsrName) {
        this.jsrName = jsrName;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public long getZfr() {
        return zfr;
    }

    public void setZfr(long zfr) {
        this.zfr = zfr;
    }

    public String getZfrName() {
        return zfrName;
    }

    public void setZfrName(String zfrName) {
        this.zfrName = zfrName;
    }

    public String getJsr() {
        return jsr;
    }

    public void setJsr(String jsr) {
        this.jsr = jsr;
    }

    public long getTaskId() {
        return taskId;
    }

    public void setTaskId(long taskId) {
        this.taskId = taskId;
    }

}
